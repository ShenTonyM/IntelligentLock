    public String Bitmap2StrByBase64(Bitmap bit){
        ByteArrayOutputStream bos=new ByteArrayOutputStream();
        bit.compress(Bitmap.CompressFormat.JPEG, 40, bos);//参数100表示不压缩
        byte[] bytes=bos.toByteArray();
        return Base64.encodeToString(bytes, Base64.DEFAULT);
    }

    private void sendRequestWithOkHttp(final String imgBase64, final String username) {
        new Thread(new Runnable() {
            @Override
            public void run() {
                try{
                    // formbody的数据
                    FormBody.Builder formBody = new FormBody.Builder();//创建表单请求体
                    formBody.add("username",username);//传递键值对参数
                    formBody.add("image",imgBase64);

                    String url = "http://101.132.165.232:8000/maoshen";

                    // Http Code
                    OkHttpClient client = new OkHttpClient.Builder()
                            .connectTimeout(10, TimeUnit.SECONDS)
                            .writeTimeout(10, TimeUnit.SECONDS)
                            .readTimeout(30, TimeUnit.SECONDS)
                            .build();
                    Request request = new Request.Builder()
                            .url(url)
                            .post(formBody.build())
                            .build();
                    Response response = client.newCall(request).execute();
                    final int resonseCode = response.code();
                    final String responseData = response.body().string();
                    Log.d("ResponseCode", String.valueOf(resonseCode));
                    Log.d("Result", responseData);
                    runOnUiThread(new Runnable() {
                        @Override
                        public void run() {
                            // 在这里进行UI操作，将结果显示到界面上
                            Toast.makeText(PhotoActivity.this, String.valueOf(resonseCode), Toast.LENGTH_SHORT).show();
                            Toast.makeText(PhotoActivity.this, responseData, Toast.LENGTH_SHORT).show();
                        }
                    });
                }catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }).start();
    }
