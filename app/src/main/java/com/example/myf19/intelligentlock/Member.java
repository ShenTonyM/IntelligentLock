package com.example.myf19.intelligentlock;

public class Member {

    private String name;

    private int imageId;

    public Member(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
